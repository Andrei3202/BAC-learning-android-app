package com.example.proiectfinal;

public class Fizicalist {
    private String name;
    private String definitie;
    private String formula;
    private String unitatedemasurare;
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getdefinitie() {
        return definitie;
    }
    public void setdefinitie(String definitie) {
        this.definitie = definitie;
    }
    public String getformula() {
        return formula;
    }
    public void setformula(String formula) {
        this.formula = formula;
    }
    public String getunitatedemasurare() {
        return unitatedemasurare;
    }
    public void setunitatedemasurare(String unitatedemasurare) {
        this.unitatedemasurare = unitatedemasurare;
    }
}
